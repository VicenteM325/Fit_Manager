    private void btnRegistrarAsistenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarAsistenciaActionPerformed
        try {
            String seleccionado = (String) jComboBoxClientesId.getSelectedItem();
            if (seleccionado == null || seleccionado.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente.");
                return;
            }
            int idCliente = Integer.parseInt(seleccionado.split(" - ")[0]);

            if (txtIdSucursal.getText().trim().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar el ID de la sucursal.");
                return;
            }
            int idSucursal = Integer.parseInt(txtIdSucursal.getText().trim());

            asistenciaController.registrarAsistencia(idCliente, idSucursal);

            javax.swing.JOptionPane.showMessageDialog(this, "Asistencia registrada correctamente.");
        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Error registrando asistencia:\n" + e.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE
            );
        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "El ID ingresado no es v√°lido.");
        }
    }//GEN-LAST:event_btnRegistrarAsistenciaActionPerformed

    private void txtNombreUserKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreUserKeyReleased
         String texto = txtNombreUser.getText().trim();
        if (!texto.isEmpty()) {
            List<Cliente> resultados = new ArrayList<>();
             try {
                 jComboBoxClientesId.setVisible(true);
                 jLabelCoincidencias.setVisible(true);
                 resultados = clienteController.buscarClientesPorNombre(texto);
             } catch (SQLException ex) {
                 System.getLogger(Asistencias.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
             }

            jComboBoxClientesId.removeAllItems();
            for (Cliente c : resultados) {
                jComboBoxClientesId.addItem(c.getIdCliente() + " - " + c.getNombre());
            }
        } else {
            jComboBoxClientesId.removeAllItems();
        }
    }//GEN-LAST:event_txtNombreUserKeyReleased

